/* 
CREATES A SQL JOB TO RUN WHEN THE SERVICE STARTS
AND SETS THE MAX MEMORY FRO SQL SERVER TO 85% OF THE TOTAL MEMORY

THIS ASSUMES THE DBA EMAIL OPERATOR EXISTS FOR JOB ALERTS 
*/

USE [msdb]
GO

/****** Object:  Job [Set Max Memory]    Script Date: 3/12/2024 9:29:45 AM ******/
BEGIN TRANSACTION
DECLARE @ReturnCode INT
SELECT @ReturnCode = 0
/****** Object:  JobCategory [[Uncategorized (Local)]]    Script Date: 3/12/2024 9:29:45 AM ******/
IF NOT EXISTS (SELECT name FROM msdb.dbo.syscategories WHERE name=N'[Uncategorized (Local)]' AND category_class=1)
BEGIN
EXEC @ReturnCode = msdb.dbo.sp_add_category @class=N'JOB', @type=N'LOCAL', @name=N'[Uncategorized (Local)]'
IF (@@ERROR <> 0 OR @ReturnCode <> 0) GOTO QuitWithRollback

END

DECLARE @jobId BINARY(16)
EXEC @ReturnCode =  msdb.dbo.sp_add_job @job_name=N'Set Max Memory', 
		@enabled=1, 
		@notify_level_eventlog=0, 
		@notify_level_email=2, 
		@notify_level_netsend=0, 
		@notify_level_page=0, 
		@delete_level=0, 
		@description=N'No description available.', 
		@category_name=N'[Uncategorized (Local)]', 
		@owner_login_name=N'sa', 
		@notify_email_operator_name=N'DBA', @job_id = @jobId OUTPUT
IF (@@ERROR <> 0 OR @ReturnCode <> 0) GOTO QuitWithRollback
/****** Object:  Step [Set Max Memory]    Script Date: 3/12/2024 9:29:45 AM ******/
EXEC @ReturnCode = msdb.dbo.sp_add_jobstep @job_id=@jobId, @step_name=N'Set Max Memory', 
		@step_id=1, 
		@cmdexec_success_code=0, 
		@on_success_action=1, 
		@on_success_step_id=0, 
		@on_fail_action=2, 
		@on_fail_step_id=0, 
		@retry_attempts=0, 
		@retry_interval=0, 
		@os_run_priority=0, @subsystem=N'TSQL', 
		@command=N'exec sys.sp_configure ''show advanced options'', 1
GO
RECONFIGURE
GO

declare @maxmemory int

select @maxmemory = (total_physical_memory_kb/1024)*.85 from sys.dm_os_sys_memory

select @maxmemory


EXEC sys.sp_configure N''max server memory (MB)'', @maxmemory
GO
RECONFIGURE WITH OVERRIDE
GO
